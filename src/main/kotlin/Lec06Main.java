public class Lec06Main {

    public static void main(String[] args) {

        for (int i = 1; i <= 5; i += 2) {
            System.out.println(i);
        }

        //내려가는 경우
        for(int i = 3; i >= 1; i--){
            System.out.println(i);
        }

        int i = 1;
        while (i <= 3) {
            System.out.println(i);
            i++;
        }

    }

}
